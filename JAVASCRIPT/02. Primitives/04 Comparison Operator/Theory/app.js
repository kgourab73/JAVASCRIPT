// ***********************************
// RELATIONAL OPERATORS
// > Greater Then
// < Less Then
// >= Greater Then or equal to
// <= Less Then or equal to

console.log(8 > 7);
console.log(8 < 7);
console.log(8 >= 7);
console.log(8 <= 7);

// ***********************************
// EQUALITY OPERATORS
// === strict equality (Type + Value)
// !== strict non-equality (Type + Value)
// == Lose equality (values)
// != Lose equality (values)

console.log(8 === 7);
console.log(8 !== 7);
console.log(8 == 7);
console.log(8 != 7);
